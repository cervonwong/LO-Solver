import { Agent } from '@mastra/core/agent';
import { Memory } from '@mastra/memory';
import { LibSQLStore } from '@mastra/libsql';
import { UnicodeNormalizer } from '@mastra/core/processors';
import { VOCABULARY_EXTRACTOR_INSTRUCTIONS } from './vocabulary-extractor-instructions';

export const vocabularyExtractorAgent = new Agent({
  name: 'Vocabulary Extractor Agent',
  instructions: VOCABULARY_EXTRACTOR_INSTRUCTIONS,
  model: 'openrouter/openai/gpt-5-mini',
  tools: {},
  inputProcessors: [
    new UnicodeNormalizer({
      stripControlChars: false,
      preserveEmojis: true,
      collapseWhitespace: true,
      trim: true,
    }),
  ],
  memory: new Memory({
    storage: new LibSQLStore({
      url: 'file:../../mastra.db', // path is relative to the .mastra/output directory
    }),
  }),
});
